$(function(){
	$("#man_tool").load("/ktdm/static_view/tool.html",function(){});
	$("#wins").html("");
	$("#wins").load("/ktdm/static_view/user_add.html",function(){});
	$("#wins").load("/ktdm/static_view/user_edit.html",function(){});
	$("#wins").load("/ktdm/static_view/man_query.html",function(){});
	$("#man_table").datagrid({
		url:"/ktdm/userInfo_manInfo.action",
		fit:true,
		closeable:true,
		autoRowHeight:false,
		fitColumns:true,
		striped:true,
		rownumbers:true,
		border:false,
		pagination:true,
		pageSize:3,
		pageList:[3,5],
		pageNumber:1,
		pagePosition:'bottom',
		sortName:'uid',
		sortOrder:"desc",
		toolbar:'#man_tool',
		showFooter:true,
		columns:[[
			{
				field:'uid',
				title:'编号',
				/*width:100,*/
				checkbox:true
			},
			{
				field:'uname',
				title:'管理员姓名',
				width:100,
			},
			{
				field:'sex',
				title:'性别',
				width:100,
			},
			{
				field:'age',
				title:'年龄',
				width:100,
			},
			
		]],
	});
	
	
	tool={
		add:function(){
			console.log($("#user_add"));
			$("#user_edit").dialog('open');
		},
		edit:function(){
			var rows=$('#man_table').datagrid('getSelections');
			if(rows.length>1){
				$.messager.alert('WARNING!','编辑记录只能选定一条数据!','warning');
			}else if(rows.length==1){
				//console.log(rows);
				$.ajax({
					url:"/ktdm/info_manCurrent.action",
					type:"POST",
					data:{
						id:rows[0].uid,
					},
					beforeSend:function(){
						$.messager.progress({
							text:'数据获取中....',
						});
					},
					success:function(data,response,status){
						$.messager.progress('close');
						//console.log(data);
						if(data.result){
							
							$("#user_edit").form('load',{
								name:data.result.uname,
								age:data.result.age,
								sex:data.result.sex,
								id:data.result.uid,
								password:data.result.password,
							}).dialog('open');
						}else{
							$.messager.alert('数据获取失败!','未知错误导致失败,请重试!','warning');
						}
					}
				});
			}else{
				$.messager.alert('WARNING!','编辑记录至少选定一条数据!','warning');
			}
		},
		remove:function(){
			var rows=$("#man_table").datagrid('getSelections');
			if(rows.length>0){
				$.messager.confirm('确定操作','确定删除所选记录?',function(flag){
					if(flag){
						var ids=[];
						for(var i=0;i<rows.length;i++){
							ids.push(rows[i].uid);
						}
						
						$.ajax({
							type:'POST',
							url:'/ktdm/userRemove_rmMan.action',
							data:{
								ids:ids.join(','),
							},
							beforeSend:function(){
								$("#man_table").datagrid('loading');
							},
							success:function(data){
								$("#man_table").datagrid('loaded');
								$("#man_table").datagrid('load');
								$("#man_table").datagrid('unselectAll');
								$.messager.show({
									title:'提示',
									msg:data.result+'条记录删除成功!',
								});
							},
						});
					}else{
						$("#man_table").datagrid('unselectAll');
					}
				});
			}else{
				$.messager.alert('提示','请选择要删除的记录!','info');
			}
		},
		search:function(){
			$("#man_query").dialog("open");
		},
	}
	$.parser.parse("#man_tool");
	$.parser.parse();
})